package modelo;

public class DCita 
{
    private int id_dcita;
    private String fecha_dcita;
    private String hinicial;
    private String hfinal;
    private int id_serv;
    private int id_sucursal;
    private int id_mcita;
    private int id_mcitaemp;
    
    
    public DCita(){}

    public int getId_dcita() {
        return id_dcita;
    }

    public void setId_dcita(int id_dcita) {
        this.id_dcita = id_dcita;
    }

    public String getFecha_dcita() {
        return fecha_dcita;
    }

    public void setFecha_dcita(String fecha_dcita) {
        this.fecha_dcita = fecha_dcita;
    }

    public String getHinicial() {
        return hinicial;
    }

    public void setHinicial(String hinicial) {
        this.hinicial = hinicial;
    }

    public String getHfinal() {
        return hfinal;
    }

    public void setHfinal(String hfinal) {
        this.hfinal = hfinal;
    }

    public int getId_serv() {
        return id_serv;
    }

    public void setId_serv(int id_serv) {
        this.id_serv = id_serv;
    }

    public int getId_sucursal() {
        return id_sucursal;
    }

    public void setId_sucursal(int id_sucursal) {
        this.id_sucursal = id_sucursal;
    }

    public int getId_mcita() {
        return id_mcita;
    }

    public void setId_mcita(int id_mcita) {
        this.id_mcita = id_mcita;
    }

    public int getId_mcitaemp() {
        return id_mcitaemp;
    }

    public void setId_mcitaemp(int id_mcitaemp) {
        this.id_mcitaemp = id_mcitaemp;
    }
    
    
    
}
