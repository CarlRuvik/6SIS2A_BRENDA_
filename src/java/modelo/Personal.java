package modelo;

public class Personal
{
    
        private int id_emp;
        private String nombre_emp;
        private String apat_emp;
        private String amat_emp;
        private int id_tipoemp;
    
    public Personal(){}

    public int getId_emp() {
        return id_emp;
    }

    public void setId_emp(int id_emp) {
        this.id_emp = id_emp;
    }

    public String getNombre_emp() {
        return nombre_emp;
    }

    public void setNombre_emp(String nombre_emp) {
        this.nombre_emp = nombre_emp;
    }

    public String getApat_emp() {
        return apat_emp;
    }

    public void setApat_emp(String apat_emp) {
        this.apat_emp = apat_emp;
    }

    public String getAmat_emp() {
        return amat_emp;
    }

    public void setAmat_emp(String amat_emp) {
        this.amat_emp = amat_emp;
    }

    public int getId_tipoemp() {
        return id_tipoemp;
    }

    public void setId_tipoemp(int id_tipoemp) {
        this.id_tipoemp = id_tipoemp;
    }
    
    
    
}
